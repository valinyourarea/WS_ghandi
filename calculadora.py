

class Calculadora():
    def __init__ (self, a, b):
        self.a = a
        self.b = b 

    def sumar(self):
        return self.a + self.b

    def restar(self):
        return self.a - self.b

    def multiplicar(self):
        return self.a * self.b

    def dividir(self):
        return self.a / self.b
